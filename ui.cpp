// ---------- state -----------------------------------------------------------
static constexpr int kCharScale  = 1;     // pixel size of each font pixel (was 2)
static constexpr int kCharW      = 8 * kCharScale;
static constexpr int kCharH      = 8 * kCharScale;
static constexpr int kRowH       = kCharH + 4;
static constexpr int kPadX       = 6;
static constexpr int kTitleH     = kCharH + 6;
static constexpr int kCheckBox   = 10;   // checkbox square
static constexpr int kSliderTrk  = 4;    // slider track thickness
static constexpr int kSliderW    = 80;
static constexpr int kSliderH    = 10;
static constexpr int kButtonH    = kCharH + 6;

struct State {
    Input in {};
    int   panelX = 0, panelY = 0, panelW = 0, panelH = 0;
    int   cursorY = 0;
    bool  inPanel = false;
    bool  wants_mouse = false;
    // persistent across frames
    const void* activeSliderId = nullptr;
};

static State g;

// ---------- helpers ---------------------------------------------------------
static inline bool pointInRect(int px, int py, int x, int y, int w, int h) {
    return px >= x && px < x + w && py >= y && py < y + h;
}

static void fillRect(int x, int y, int w, int h, float r, float g_, float b, float a = 1.0f) {
    glColor4f(r, g_, b, a);
    glBegin(GL_QUADS);
    glVertex2f((float)x,       (float)y);
    glVertex2f((float)(x + w), (float)y);
    glVertex2f((float)(x + w), (float)(y + h));
    glVertex2f((float)x,       (float)(y + h));
    glEnd();
}

static void strokeRect(int x, int y, int w, int h, float r, float g_, float b) {
    glColor3f(r, g_, b);
    glBegin(GL_LINE_LOOP);
    glVertex2f((float)x + 0.5f,       (float)y + 0.5f);
    glVertex2f((float)(x + w) - 0.5f, (float)y + 0.5f);
    glVertex2f((float)(x + w) - 0.5f, (float)(y + h) - 0.5f);
    glVertex2f((float)x + 0.5f,       (float)(y + h) - 0.5f);
    glEnd();
}

static int textWidth(const char* s) {
    return (int)std::strlen(s) * kCharW;
}

static void drawText(int x, int y, const char* s, float r, float g_, float b) {
    glColor3f(r, g_, b);
    glBegin(GL_QUADS);
    int px = x;
    for (; *s; ++s) {
        unsigned char c = (unsigned char)*s;
        if (c < 128) {
            const unsigned char* glyph = font8x8[c];
            for (int row = 0; row < 8; ++row) {
                unsigned char rowByte = glyph[row];
                if (!rowByte) continue;
                for (int col = 0; col < 8; ++col) {
                    if (rowByte & (1u << col)) {
                        float x0 = (float)(px + col * kCharScale);
                        float y0 = (float)(y  + row * kCharScale);
                        float x1 = x0 + kCharScale;
                        float y1 = y0 + kCharScale;
                        glVertex2f(x0, y0);
                        glVertex2f(x1, y0);
                        glVertex2f(x1, y1);
                        glVertex2f(x0, y1);
                    }
                }
            }
        }
        px += kCharW;
    }
    glEnd();
}

// ---------- projection ------------------------------------------------------
void setProjectionToPixels(int width, int height) {
    glMatrixMode(GL_PROJECTION);
    glPushMatrix();
    glLoadIdentity();
    glOrtho(0.0, (double)width, (double)height, 0.0, -1.0, 1.0); // y-down
    glMatrixMode(GL_MODELVIEW);
    glPushMatrix();
    glLoadIdentity();
    glDisable(GL_DEPTH_TEST);
}

void restoreProjection() {
    glMatrixMode(GL_PROJECTION);
    glPopMatrix();
    glMatrixMode(GL_MODELVIEW);
    glPopMatrix();
}

// ---------- API -------------------------------------------------------------
void begin(const Input& in) {
    g.in = in;
    g.wants_mouse = false;
}

bool wantsMouse() { return g.wants_mouse; }

void beginPanel(int x, int y, int w, int h, const char* title) {
    g.panelX = x;
    g.panelY = y;
    g.panelW = w;
    g.panelH = h;
    g.cursorY = y + kTitleH + 4;
    g.inPanel = true;

    if (pointInRect(g.in.mouseX, g.in.mouseY, x, y, w, h))
        g.wants_mouse = true;

    // backdrop
    fillRect(x, y, w, h, 0.10f, 0.10f, 0.13f, 0.85f);
    strokeRect(x, y, w, h, 0.6f, 0.6f, 0.7f);
    // title bar
    fillRect(x, y, w, kTitleH, 0.18f, 0.20f, 0.28f, 0.95f);
    if (title) drawText(x + kPadX, y + 4, title, 0.95f, 0.95f, 0.95f);
}

void endPanel() {
    g.inPanel = false;
}

void text(const char* fmt, ...) {
    char buf[256];
    va_list ap; va_start(ap, fmt);
    std::vsnprintf(buf, sizeof(buf), fmt, ap);
    va_end(ap);
    drawText(g.panelX + kPadX, g.cursorY, buf, 0.9f, 0.9f, 0.9f);
    g.cursorY += kRowH;
}

bool checkbox(const char* label, bool* value) {
    int x = g.panelX + kPadX;
    int y = g.cursorY;
    int boxY = y + (kCharH - kCheckBox) / 2;
    bool hover = pointInRect(g.in.mouseX, g.in.mouseY,
                             x, y, g.panelW - 2 * kPadX, kCharH);
    bool changed = false;
    if (hover && g.in.mousePressed) {
        *value = !*value;
        changed = true;
    }
    // box
    fillRect(x, boxY, kCheckBox, kCheckBox,
             hover ? 0.30f : 0.22f, hover ? 0.32f : 0.22f, hover ? 0.36f : 0.26f);
    strokeRect(x, boxY, kCheckBox, kCheckBox, 0.7f, 0.7f, 0.8f);
    if (*value) {
        // filled check mark
        fillRect(x + 3, boxY + 3, kCheckBox - 6, kCheckBox - 6,
                 0.3f, 0.85f, 0.4f);
    }
    drawText(x + kCheckBox + 8, y, label, 0.92f, 0.92f, 0.92f);
    g.cursorY += kRowH;
    return changed;
}

bool slider(const char* label, float* value, float lo, float hi) {
    int x = g.panelX + kPadX;
    int y = g.cursorY;
    drawText(x, y, label, 0.92f, 0.92f, 0.92f);
    int labelW = textWidth(label);
    char vbuf[32];
    std::snprintf(vbuf, sizeof(vbuf), "%.2f", *value);
    drawText(x + g.panelW - 2 * kPadX - textWidth(vbuf), y, vbuf,
             0.92f, 0.92f, 0.92f);
    (void)labelW;

    int trkY = y + kCharH + 2;
    int trkX = x;
    int trkW = g.panelW - 2 * kPadX;
    // track
    fillRect(trkX, trkY + (kSliderH - kSliderTrk) / 2,
             trkW, kSliderTrk, 0.18f, 0.18f, 0.22f);
    strokeRect(trkX, trkY + (kSliderH - kSliderTrk) / 2,
               trkW, kSliderTrk, 0.55f, 0.55f, 0.65f);
    // knob
    float t = (*value - lo) / (hi - lo);
    if (t < 0) t = 0;
    if (t > 1) t = 1;
    int knobW = 10;
    int knobX = trkX + (int)((trkW - knobW) * t);
    int knobY = trkY;

    bool hover = pointInRect(g.in.mouseX, g.in.mouseY,
                             trkX, trkY, trkW, kSliderH);
    if (hover && g.in.mousePressed) g.activeSliderId = (const void*)label;
    if (g.in.mouseReleased && g.activeSliderId == (const void*)label)
        g.activeSliderId = nullptr;

    bool changed = false;
    if (g.activeSliderId == (const void*)label) {
        float nt = (float)(g.in.mouseX - trkX) / (float)(trkW - knobW);
        if (nt < 0) nt = 0;
        if (nt > 1) nt = 1;
        float nv = lo + nt * (hi - lo);
        if (nv != *value) { *value = nv; changed = true; }
        // refresh visual t for this same frame
        t = (*value - lo) / (hi - lo);
        if (t < 0) t = 0;
    if (t > 1) t = 1;
        knobX = trkX + (int)((trkW - knobW) * t);
    }

    bool active = (g.activeSliderId == (const void*)label);
    fillRect(knobX, knobY, knobW, kSliderH,
             active ? 0.95f : 0.78f,
             active ? 0.78f : 0.78f,
             active ? 0.35f : 0.35f);
    strokeRect(knobX, knobY, knobW, kSliderH, 0.95f, 0.85f, 0.45f);

    g.cursorY += kRowH + kSliderH;
    return changed;
}

bool button(const char* label) {
    int x = g.panelX + kPadX;
    int y = g.cursorY;
    int w = g.panelW - 2 * kPadX;
    int h = kButtonH;
    bool hover = pointInRect(g.in.mouseX, g.in.mouseY, x, y, w, h);
    bool pressed = hover && g.in.mouseDown;
    fillRect(x, y, w, h,
             pressed ? 0.30f : (hover ? 0.35f : 0.25f),
             pressed ? 0.38f : (hover ? 0.42f : 0.30f),
             pressed ? 0.55f : (hover ? 0.60f : 0.42f));
    strokeRect(x, y, w, h, 0.75f, 0.78f, 0.9f);
    int tx = x + (w - textWidth(label)) / 2;
    int ty = y + (h - kCharH) / 2;
    drawText(tx, ty, label, 0.98f, 0.98f, 0.98f);
    g.cursorY += h + 6;
    return hover && g.in.mousePressed;
}

